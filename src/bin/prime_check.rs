fn find_generator(prime: u64) -> Result<u64, anyhow::Error> {
    let mut candidate: u64 = 2;

    while candidate < prime {
        let mut power = modulo_mul(candidate, candidate, prime);
        let mut count = 1;
        while power != candidate {
            power = modulo_mul(power, candidate, prime);
            count += 1;
            if count > prime - 1 {
                break;
            }
        }

        if count == prime - 1 {
            return Ok(candidate);
        }

        candidate += 1;
    }

    Err(anyhow!("failed to find generator for prime {prime}"))
}

fn compute_fri_rounds(prime: u64) -> usize {
    let mut fri_rounds = 0;
    let mut order = prime - 1;
    while order.is_multiple_of(4) {
        fri_rounds += 1;
        order /= 4;
    }

    fri_rounds
}
